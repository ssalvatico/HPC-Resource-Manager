/**
 * [INTEGRATION]
 * Where to call: Inside the Patcher (resource_adapter.c) when preparing to broadcast 
 * a CMD_ANNOUNCE via UDP, or when formatting a GET_NODES response.
 */
void get_local_resources(local_resources_t * resources, unsigned * cpu_quantity, unsigned * gpu_quantity, unsigned * mem_quantity){
    if(cpu_quantity) { * cpu_quantity = resources -> avail[CPU]; }    
    if(gpu_quantity) { * gpu_quantity = resources -> avail[GPU]; }
    if(mem_quantity) { * mem_quantity = resources -> avail[RAM]; }    
}