/* PHYSICAL INVENTORY MANAGEMENT                                             */
/* ========================================================================= */

/**
 * [INTEGRATION] 
 * Where to call: Should be called ONLY ONCE during node initialization (e.g., inside 'node_init')
 * to define the physical hardware boundaries of this specific node.
 */
local_resources_t * create_local_resource( unsigned cpu,  unsigned gpu,  unsigned ram){
    local_resources_t * out = malloc(sizeof(local_resources_t));
    if (out == NULL) return NULL;
    
    out -> total[CPU] = cpu;
    out -> total[GPU] = gpu;
    out -> total[RAM] = ram;
    
    for( unsigned t = 0 ; t < RES_NUM ; t++) { 
        out -> avail[t] = out -> total[t]; 
        out -> request_queue[t] = queue_create(); 
    }

    return out;
}

/**
 * [INTEGRATION] 
 * Where to call: Should be called during graceful shutdown (e.g., inside 'node_dest')
 * to free the main structure and all pending elements in the queues to prevent memory leaks.
 */
void delete_local_resource(local_resources_t * resources){
    if (resources == NULL) return;
    
    for (unsigned idx = 0 ; idx < RES_NUM ; idx++) { 
        queue_destroy(resources -> request_queue[idx], (FuncionDestructora)dest_job_request); 
    }
    free(resources);
}


/* ========================================================================= */
/* RESERVATIONS & WAITING QUEUE LOGIC                                        */
/* ========================================================================= */

/**
 * [INTEGRATION]
 * Where to call: Inside the Patcher (resource_adapter.c) when parsing a CMD_RESERVE.
 * Example: int result = new_job_request(NODE->resources, NODE->active_jobs, job_id, socket, quantity, type);
 * If result == -1 -> send DENIED.
 * If result == 1  -> send GRANTED.
 * If result == 0  -> Do nothing (it is queued).
 */
int new_job_request (local_resources_t * resources, active_jobs_t active_jobs, unsigned id, unsigned socket, unsigned quantity, resource_t type){
    
    // 1. HARD REJECT: Request exceeds the maximum physical capacity of the machine.
    if (quantity > resources->total[type]) {
        return -1; 
    }

    // 2. QUEUE: Valid request, but not enough resources currently available. Enqueue it.
    if(resources -> avail[type] < quantity){
        elem_job_request_t * new = create_job_request(id, socket, quantity, type);
        if(new != NULL)
            resources -> request_queue[type] = queue_add(resources -> request_queue[type], new, (FuncionCopiadora)copy_job_request);
        return 0; 
    }
    
    // 3. IMMEDIATE GRANT: Resources available. Deduct inventory and track the active job.
    resources -> avail[type] -= quantity;
    tablahash_insertar(active_jobs, create_elem_active_job(id, socket, quantity, type));

    return 1; 
}

/**
 * [INTEGRATION]
 * Where to call: Inside the Patcher (resource_adapter.c) IMMEDIATELY AFTER freeing resources 
 * (after a CMD_RELEASE or an ACTION_DISCONNECTED) to check if any queued node can now be served.
 * Important: This should be called in a while loop until it returns -1, as freeing 
 * a large chunk of resources might fulfill multiple smaller queued requests.
 */
int chk_job_request (local_resources_t * resources, active_jobs_t active_jobs, char * OUT, unsigned OUT_SIZE, unsigned *out_socket){
    queue_t                 act         = NULL;
    elem_job_request_t  * act_elem    = NULL;
    int out = -1;    
    
    // Randomize the starting resource type to prevent starvation of a specific queue
    resource_t init = rand() % RES_NUM;
    resource_t type;

    for(resource_t idx = 0; idx < RES_NUM && out == -1 ; idx++){
        type = (init + idx) % RES_NUM;
        act = resources -> request_queue[type];
        
        do{ 
            if(queue_empty(act)) { break; }

            act_elem = act -> data;

            // If still not enough resources for this specific request, skip it and check the next one
            if(resources -> avail[type] < act_elem -> resource_quantity) { 
                act = act -> next; 
                continue; 
            }

            // Resources found! Fulfill the request, deduct inventory, and track the job
            resources -> avail[type] -= act_elem -> resource_quantity;
            out = act_elem -> job_id;

            if (out_socket != NULL)
                *out_socket = act_elem -> socket;

            tablahash_insertar(active_jobs, create_elem_active_job(act_elem -> job_id, act_elem->socket, act_elem ->resource_quantity, type));

            // Remove it from the waiting queue
            resources -> request_queue[type] = queue_delete(resources -> request_queue[type], act_elem, (FuncionDestructora)dest_job_request, (FuncionComparadora)comp_job_request);

            break; // Exit the queue traversal since we fulfilled one request
        }while(act != NULL);
    }

    if (out != -1) {
        snprintf(OUT, OUT_SIZE, "GRANTED %d\n", out);
    }

    return out;
}


