/**
 * [INTEGRATION]
 * Where to call: Inside the Patcher (resource_adapter.c) when handling a CMD_RELEASE.
 * This restores the specific physical resources to the node's available pool.
 */
void del_active_job (local_resources_t * resources, active_jobs_t jobs, unsigned job_id, unsigned socket){
    for (resource_t type = 0; type < RES_NUM; type++) {
        // Create a dummy element using the composite key (job_id + socket) to find it
        elem_active_job_t * dummy = create_elem_active_job(job_id, socket, 0, type);
        if(dummy == NULL) return;

        elem_active_job_t * job_actual = tablahash_buscar(jobs, dummy);

        if(job_actual != NULL) {
            // Restore inventory
            resources->avail[job_actual -> resource_type] += job_actual -> resource_quantity;
            tablahash_eliminar(jobs, dummy);
        }

        dest_active_job(dummy);
    }
}

/**
 * [INTEGRATION]
 * Where to call: Inside the Patcher (resource_adapter.c) when handling a CMD_RELEASE 
 * or CMD_JOB_RELEASE, to remove requests that were queued but are now being aborted by the client.
 */
void del_pending_job_requests(local_resources_t * resources, unsigned job_id, unsigned socket){
    for (resource_t type = 0; type < RES_NUM; type++) {
        queue_t act = resources -> request_queue[type];

        while (!queue_empty(act)) {
            elem_job_request_t * elem = act -> data;

            // Strict match required to avoid deleting someone else's request with the same job_id
            if (elem -> job_id == job_id && elem->socket == socket) {
                resources -> request_queue[type] = queue_delete(
                    resources -> request_queue[type],
                    elem,
                    (FuncionDestructora)dest_job_request,
                    (FuncionComparadora)comp_job_request
                );
                act = resources -> request_queue[type]; // Reset iterator after structural modification
            } else {
                act = act -> next;
            }
        }
    }
}

// Visitor function used internally by free_all_resources_from_socket to scan the hash table
static void check_dead_socket(void * dato, void * extra) {
    elem_active_job_t * job = (elem_active_job_t *)dato;
    sweep_context_t * ctx = (sweep_context_t *)extra;

    if (job->socket == ctx->target_socket && ctx->count < ctx->max) {
        ctx->to_delete[ctx->count++] = job;
    }
}

/**
 * [INTEGRATION]
 * Where to call: Inside the Patcher (resource_adapter.c) strictly during an ACTION_DISCONNECTED event.
 * This is the Garbage Collector function that prevents deadlocks when a remote node crashes.
 */
void free_all_resources_from_socket(local_resources_t * resources, active_jobs_t jobs, unsigned socket) {
    
    // --- PHASE A: Purge ACTIVE JOBS (Resources currently held hostage by the dead node) ---
    sweep_context_t ctx;
    ctx.target_socket = socket;
    ctx.count = 0;
    ctx.max = 100; // Assumption: A single node won't hold more than 100 concurrent resource chunks
    ctx.to_delete = malloc(sizeof(elem_active_job_t *) * ctx.max);
    
    if (ctx.to_delete != NULL) {
        // We pass 'ctx' by reference. The visitor will populate ctx.to_delete with the findings.
        tablahash_visitar_extra(jobs, check_dead_socket, &ctx);
        
        for (unsigned i = 0; i < ctx.count; i++) {
            elem_active_job_t * dead_job = ctx.to_delete[i];
            
            // Reclaim resources to the local pool
            resources->avail[dead_job->resource_type] += dead_job->resource_quantity;
            tablahash_eliminar(jobs, dead_job);
        }
        free(ctx.to_delete);
    }

    // --- PHASE B: Purge PENDING JOBS (Requests clogging the queues from the dead node) ---
    for (resource_t type = 0; type < RES_NUM; type++) {
        queue_t act = resources->request_queue[type];

        while (!queue_empty(act)) {
            elem_job_request_t * elem = act->data;

            if (elem->socket == socket) {
                // Delete using the native queue function, but using our custom socket-only comparator
                resources->request_queue[type] = queue_delete(
                    resources->request_queue[type],
                    &socket, // Passed as reference to be used by 'comp_job_request_by_socket'
                    (FuncionDestructora)dest_job_request,
                    (FuncionComparadora)comp_job_request_by_socket
                );
                // Restart traversal from the head of the queue since elements shifted
                act = resources->request_queue[type]; 
            } else {
                act = act->next;
            }
        }
    }
}


