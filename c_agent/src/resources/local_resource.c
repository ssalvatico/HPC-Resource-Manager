/**
 * @brief Represents a granted resource allocation for a specific remote node.
 * Instances of this structure are stored in the 'active_jobs_t' hash table.
 * The combination of 'job_id' and 'socket' serves as the unique primary key 
 * to prevent collisions between different nodes generating the same job_id.
 */
typedef struct{
    unsigned    job_id;             /**< Identifier generated by the remote Erlang scheduler */
    unsigned    socket;             /**< File descriptor representing the remote node connection */
    unsigned    resource_quantity;  /**< Amount of the resource that was granted */
    resource_t  resource_type;      /**< The specific hardware type (CPU, GPU, RAM) */
} elem_active_job_t;

/**
 * @brief Represents a pending request waiting for resources to become available.
 * Instances of this structure are stored in the FIFO queues. They hold the 
 * state of a reservation that could not be fulfilled immediately.
 */
typedef struct{
    unsigned    job_id;             /**< Identifier generated by the remote Erlang scheduler */
    unsigned    socket;             /**< File descriptor representing the remote node connection */
    unsigned    resource_quantity;  /**< Amount of the resource requested */
    resource_t  resource_type;      /**< The specific hardware type (CPU, GPU, RAM) */
} elem_job_request_t;

/**
 * @brief Temporary context passed through a void pointer during hash table traversal.
 * Used exclusively by 'free_all_resources_from_socket' to safely locate and collect 
 * all active jobs associated with a dead socket without relying on global variables.
 */
typedef struct {
    unsigned target_socket;             /**< The socket of the disconnected node we are looking for */
    elem_active_job_t ** to_delete;     /**< Array of pointers to collect the matches found */
    unsigned count;                     /**< Current number of matched elements found */
    unsigned max;                       /**< Maximum capacity of the 'to_delete' array */
} sweep_context_t;


/* ========================================================================= */
/* INTERNAL HELPERS - ACTIVE JOBS (HASH TABLE)                               */
/* ========================================================================= */

static void     dest_active_job     (elem_active_job_t * elem)                          { free(elem); }
static void * copy_active_job     (elem_active_job_t * elem)                          { return elem; }

// Compares both job_id and socket to ensure true unique identity across different Erlang clients
static int      comp_active_job     (elem_active_job_t * a, elem_active_job_t * b)      { 
    return (a -> job_id != b -> job_id || a -> socket != b -> socket || a -> resource_type != b -> resource_type); 
}

// Combines job_id and socket in the mathematical hash to avoid cluster collisions
static unsigned hash_active_job     (elem_active_job_t * elem)                          { 
    return ((elem -> job_id * 31) + elem -> socket) * RES_NUM + elem -> resource_type; 
}

// Internal constructor for creating a new active job element
static elem_active_job_t * create_elem_active_job( unsigned id, unsigned socket, unsigned quantity, resource_t type){
    elem_active_job_t * out = malloc(sizeof(elem_active_job_t));
    if(out == NULL) { return NULL; }

    out -> job_id               = id;
    out -> socket               = socket; 
    out -> resource_quantity    = quantity;
    out -> resource_type        = type;
    
    return out;
}

/**
 * [INTEGRATION] 
 * Where to call: Should be called ONLY ONCE during node initialization (e.g., inside 'node_init' in node-structures.c).
 */
active_jobs_t create_active_jobs_table(){
    return tablahash_crear(11, (FuncionCopiadora)copy_active_job, (FuncionComparadora)comp_active_job, (FuncionDestructora)dest_active_job, (FuncionHash)hash_active_job);
}


/* ========================================================================= */
/* INTERNAL HELPERS - PENDING JOB REQUESTS (QUEUES)                          */
/* ========================================================================= */

static void     dest_job_request    (elem_job_request_t * elem)                         { free(elem); }
static void * copy_job_request    (elem_job_request_t * elem)                         { return elem; }

// Standard comparator: Requires exact match of job_id, socket, and resource type
static int      comp_job_request    (elem_job_request_t * a, elem_job_request_t * b){ 
    return (a -> job_id != b -> job_id || a -> resource_type != b -> resource_type || a->socket != b->socket); 
}

// Custom comparator: Used by the GC to delete ALL requests from a specific socket, regardless of job_id
static int comp_job_request_by_socket (elem_job_request_t * a, unsigned * target_socket) { 
    return (a->socket != *target_socket); // Returns 0 (false) if sockets match, triggering the queue_delete
}

// Internal constructor for creating a new pending request element
static elem_job_request_t * create_job_request( unsigned id, unsigned socket, unsigned quantity, resource_t type){
    elem_job_request_t * out = malloc(sizeof(elem_job_request_t));
    if(out == NULL) { return NULL; }

    out -> job_id               = id;
    out -> socket               = socket;
    out -> resource_quantity    = quantity;
    out -> resource_type        = type;

    return out;
}


